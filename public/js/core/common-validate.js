/**
 * common-validate.js - Global jQuery Validate configuration for Bootstrap 4
 *
 * Only configures visual integration (classes, errorPlacement).
 * Messages and rules are defined in each module that calls .validate().
 *
 * @package ProyectoBase
 * @subpackage JavaScript\Core
 * @version 1.0
 */

$.validator.setDefaults({
    errorClass: 'is-invalid',
    validClass: 'is-valid',
    errorElement: 'span',

    // Do not ignore hidden selects (used by Select2)
    ignore: ':hidden:not(select)',

    // Validate only on blur and submit, not on every keystroke
    onkeyup: false,

    /**
     * Places the error message inside the field's form-group.
     * The d-block class is required because Bootstrap hides .invalid-feedback by default.
     * @param {jQuery} error   - Error span generated by jQuery Validate
     * @param {jQuery} element - Invalid field
     */
    errorPlacement: function (error, element) {
        error.addClass('invalid-feedback d-block');
        element.closest('.form-group').append(error);
    },

    highlight: function (element, errorClass, validClass) {
        $(element).addClass(errorClass).removeClass(validClass);
    },

    unhighlight: function (element, errorClass, validClass) {
        $(element).removeClass(errorClass).addClass(validClass);
    },

    /** Clear message when field becomes valid */
    success: function (label, element) {
        $(element).removeClass('is-invalid').addClass('is-valid');
    },

    /**
     * Default submit: submit the native form.
     * Each module can override with its own submitHandler.
     * @param {HTMLFormElement} form
     */
    submitHandler: function (form) {
        form.submit();
    }
});
